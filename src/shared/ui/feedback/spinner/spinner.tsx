import { cn } from "@/shared/lib/cn";

type SpinnerSize = "sm" | "md" | "lg";

const sizeMap: Record<SpinnerSize, string> = {
    sm: "h-4 w-4 border-2",
    md: "h-5 w-5 border-2",
    lg: "h-8 w-8 border-3",
};

export type SpinnerProps = {
    size?: SpinnerSize;
    className?: string;
};

export function Spinner({ size = "md", className }: SpinnerProps) {
    return (
        <span
            className={cn(
                "inline-block animate-spin rounded-full border-slate-300 border-t-transparent",
                sizeMap[size],
                className,
            )}
            aria-label="Cargando"
        />
    );
}
